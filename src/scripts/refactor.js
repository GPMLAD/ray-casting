const size = 25

class Player {
  constructor(size, pX, pY, dirX, dirY, planeX, planeY) {
    this.mousePositions = { x: 0, y: 0 }
    this.size = size
    this.position = { x: pX, y: pY }
    this.dir = { x: dirX, y: dirY }
    this.plane = { x: planeX, y: planeY }
    this.moveSpeed = 1 / size
    this.rotSpeed = Math.PI / 120
  }
}

class Map {
  constructor(matrix, size) {
    this.content = matrix
    this.size = size
  }

  clearScreen() {
    ctx.clearRect(0, 0, canvas.width, canvas.height)
  }

  drawMap() {
    for (let i = 0; i < this.content.length; i++) {
      const row = this.content[i]
      for (let j = 0; j < row.length; j++) {
        const element = row[j]
        if (element) {
          ctx.fillStyle = 'blue'
          ctx.fillRect(
            i * this.size,
            j * this.size,
            this.size - 2,
            this.size - 2
          )
        } else {
          ctx.fillStyle = 'grey'
          ctx.fillRect(
            i * this.size,
            j * this.size,
            this.size - 2,
            this.size - 2
          )
        }
      }
    }
  }
}

